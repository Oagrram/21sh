#include "libft.h"

int		ft_strclr(char *s)
{
	int i;

	i = 0;
	if (s != NULL)
	{
		while (s[i])
			i++;
		while (i >= 0)
		{
			s[i] = '\0';
			i--;
		}
	}
	return (0);
}
